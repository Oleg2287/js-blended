// логін за допомогою prompt і виводить результат в консоль браузера

// Після цього додайте перевірку введеного користувачем значення:
// Якщо відвідувач вводить "Адмін",
// то prompt запитує пароль (не забудьте зберігти його у змінну для подальшої перевірки).
// Якщо нічого не ввели або натиснули Cancel,
// Вивести в alert строку "Скасовано"
// В іншому випадку вивести в alert рядок "Я вас не знаю"

// Пароль перевіряти так:
// Якщо введено пароль "Я головний",
// то вивести в alert рядок "Добрий день!",
// в іншому випадку вивести в alert рядок "Невірний пароль!"

let login = prompt("Введіть ваш логін:"); // Запитуємо логін у користувача
if (login === "Адмін") { // Перевіряємо, чи логін дорівнює "Адмін"
    let password = prompt("Введіть ваш пароль:"); // Запитуємо пароль
    if (password === "Я головний") { // Перевіряємо, чи пароль дорівнює "Я головний"
        alert("Добрий день!"); // Якщо так, виводимо "Добрий день!"
    } else {
        alert("Невірний пароль!"); // Якщо ні, виводимо "Невірний пароль!"
    }
} else if (login === null || login.trim() === "") {
    alert("Скасовано"); // Якщо нічого не ввели або натиснули Cancel, виводимо "Скасовано"
} else {
    alert("Я вас не знаю"); // В іншому випадку виводимо "Я вас не знаю"
}

