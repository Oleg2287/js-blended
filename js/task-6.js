// Використайте цикл while, щоб вивести в console всі числа від 0 до 20 включно.
let number = 0; // оголошуємо змінну number і присвоюємо їй значення 0
while (number <= 20) {  // вказуємо умову циклу, перевіряємо, чи number менше або дорівнює 20
    console.log(number) // виводимо значення number в консоль
    number += 1 // збільшуємо значення number на 1 (додаємо щоб цикл не був безкінечним)
}


